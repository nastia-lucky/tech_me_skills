export default class Calculator {

    static doAdd(param1: number, param2: number): number {
        return param1 + param2;
    }

    static doSubstract(param1: number, param2: number) {
        return param1 - param2;
    }

    static doMultiply(param1: number, param2: number) {
        return param1 * param2;
    }

    static doDivision(param1: number, param2: number) {
        return param1 / param2;
    }

    static getRemainderDivision(param1: number, param2: number) {
        return param1 % param2;
    }






}